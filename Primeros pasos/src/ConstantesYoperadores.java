public class ConstantesYoperadores {
    public static void main(String[] args) {
        double a=5;
        double b =7;
        double c =b/a;
        final int constante = 3; //esto es una constante de tipo entero
        int d,e,f;
        d=1;
        e=2;
        f=constante+d+e;
        
        System.out.println(c);
        System.out.println("La suma de "+constante +" + "+e+ " + "+d+ " = "+f);
    }
   
}
